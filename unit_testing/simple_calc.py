class SimpleCalc:
    def add(self, a, b):
        if a + b == 0:
            return 0
        return a + b

    def subtract(self, a, b):
        if a - b == 0:
            return 0
        return a - b

    def multiply(self, a, b):
        if a * b == 0:
            return 0
        return a * b

    def divide(self, a, b):
        if a / b == 0:
            return 0
        return a / b